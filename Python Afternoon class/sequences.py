# List
list1 = ["Apple", 5, 6.7, "Orange"]
print(list1)
print(list1[2:4])
print(type(list1))

# Tuples

mytuple = ("Mary", "Sofia", 10)

print(mytuple[1])
print(mytuple[1:2])
print(mytuple*2)

#Dictionaries
examp = {1: "phone", 2: "laptop", 3: "computer"}

print(type(examp))
print(examp.keys())
print(examp.values())
print(examp[3])
